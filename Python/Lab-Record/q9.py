mytuple = ("x", "y", ["a", "b", "c"], "z")
print("mytuple:", mytuple)

emptytuple = (None,)  # A tuple with a single element
print("emptytuple:", emptytuple)

tup = (0,)  # Adding a comma to create a single-element tuple
print("tup:", tup)

t = tuple("Python")  # Creating a tuple from a string
print("t:", t)
